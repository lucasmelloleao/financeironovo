<H3>METODOLOGIA PARA DESENVOLVIMENTO </H3>
<HR>
<div class="text-justify">
    <p>Planejamento Estratégico é um conjunto de atividades desenvolvidas para se estabelecer definições e conceitos básicos que irão direcionar as ações de uma organização, contribuindo significativamente para a obtenção de melhores resultados.
    <p>É uma oportunidade para as pessoas envolvidas com a casa dedicarem um tempo e atenção para repensarem o que fazem, por que fazem e onde desejam chegar. É importante para qualquer trabalho em equipe que todos seus integrantes alcancem o mesmo nível de entendimento, razões, objetivos e a mesma visão de futuro.
    <p>O pensamento estratégico é fator fundamental para o estabelecimento de diretrizes organizacionais que irão orientar as atividades e fortalecer a competência organizacional de seus integrantes, que são colocados a pensar sobre o futuro da instituição, revendo valores, conceitos, fazendo conjecturas e simulações sobre as possíveis dificuldades e diversas oportunidades para enfrentar os seus desafios.
    <p>O processo de planejamento estratégico proporciona a visualização de um horizonte comum a todos os apoiadores da instituição, gerando um esforço coletivo e sinérgico das pessoas em prol dos objetivos organizacionais.
    <p>O produto final do Planejamento Estratégico é a criação de plano de trabalho, gerado a partir de um trabalho de estímulo para obtenção de ideias colocadas livremente por todos, posteriormente analisadas, agrupadas, priorizadas e os responsáveis definidos para apresentarem projetos com custos e prazos. Deve ser revisto anualmente e o plano de ação acompanhado mensalmente.


</div>

