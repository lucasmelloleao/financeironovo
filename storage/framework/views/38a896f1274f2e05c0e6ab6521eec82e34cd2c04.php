    <h2> DIRETRIZES DE AÇÃO </h2>
    Considerando-se que o Movimento Espírita tem por missão promover e realizar o estudo, a divulgação e a prática da Doutrina Espírita, recomenda-se que suas atividades sejam desenvolvidas dentro das seguintes diretrizes:
    <hr> 
    <p><strong> 1 – A DIFUSÃO DA DOUTRINA ESPÍRITA; </strong>
    <p> <strong>Objetivo</strong>
    <p>Difundir a Doutrina Espírita, pelo seu estudo, divulgação e prática, colocando-a ao alcance e a serviço de todas as pessoas, indistintamente, independentemente de sua condição social, cultural, econômica ou faixa etária.

    <p> <strong> 2 – A PRESERVAÇÃO DA UNIDADE DE PRINCÍPIOS DA DOUTRINA ESPÍRITA; </strong>
    <p> <strong>Objetivo </strong>
    <p>   Desenvolver todas as atividades espíritas com base nas obras básicas de Allan Kardec, que constituem a Codificação Espírita, assegurando a unidade desses princípios em todos os trabalhos realizados e divulgados como atividades espíritas. 

    <p> <strong>  3 – A COMUNICAÇÃO SOCIAL ESPÍRITA; </strong>
    <p> <strong>     Objetivos  </strong>
    <p>  Contribuir para o aprimoramento do processo de comunicação nas instituições espíritas, considerando que a comunicação, como ato natural e humano, permeia todas as atividades internas e facilita os relacionamentos individuais e coletivos. 
    <p>   Promover a difusão da Doutrina Espírita, de forma planejada e contínua, com base nos princípios doutrinários, direcionando a mensagem aos diferentes públicos, conforme as faixas etárias, os níveis culturais, sociais e econômicos, e selecionar os meios de comunicação lícitos e compatíveis com a ética preconizada pela Doutrina Espírita. 

    <p> <strong>    4 – A ADEQUAÇÃO DOS CENTROS ESPÍRITAS PARA O ATENDIMENTO DE SUAS FINALIDADES; </strong>
    <p><strong>  Objetivo </strong>
    <p>   Adequar os centros espíritas para a realização do seu trabalho de estudo, difusão e prática da Doutrina Espírita, desdobrado nas atividades doutrinárias, assistenciais, administrativas e de unificação. 
    <p> <strong>5 – A MULTIPLICAÇÃO DOS CENTROS ESPÍRITAS; </strong>
    <p> <strong>     Objetivos </strong>
    <p>Promover a implantação de novos centros espíritas, devidamente organizados e com adequada orientação doutrinária e assistencial, em locais onde se façam necessários, com a finalidade de atender à Sociedade, descentralizando e interiorizando a ação espírita. 
    <p>Estimular a criação de centros espíritas como “núcleos que se caracterizam pela simplicidade própria das primeiras casas do Cristianismo nascente”. (Op. cit. cap. Os Centros Espíritas.) 
    <p>Considerar “[...] que toda a formação espírita guarda raízes nas fontes do Cristianismo simples e claro, com finalidades morais distintas, no aperfeiçoamento da alma, expressando aquele Consolador que Jesus prometeu aos tempos novos”. (XAVIER, Francisco C. Justiça divina. Pelo Espírito Emmanuel. 13. ed. 2. reimp. FEB, 2010. cap. Invocações.) 
    <p> <strong> 6 – A UNIÃO DOS ESPÍRITAS E A UNIFICAÇÃO DO MOVIMENTO ESPÍRITA; </strong>
    <p> <strong> Objetivos </strong>
    <p>Desenvolver o trabalho de união dos espíritas e dos centros espíritas, assim como o de unificação do Movimento Espírita, como natural vivência dos ensinos espíritas e como atividade indispensável ao fortalecimento, à ampliação e ao aprimoramento da ação do Movimento Espírita em todas as suas realizações. 
    <p>Promover e realizar atividades que possibilitem a troca de informações e de experiências, a ajuda recíproca e o trabalho conjunto entre os centros espíritas. 
    <p>Promover e realizar atividades que possibilitem a troca de informações e de experiências, a ajuda recíproca e o trabalho em conjunto entre os Órgãos de Unificação, assim como entre as Entidades Especializadas. 
    <p>Oferecer condições para o conhecimento e a implementação das recomendações e campanhas aprovadas e lançadas pelo Conselho Federativo Nacional da FEB, em especial, estimulando e orientando quanto à importância da vivência evangélica na família, com base na Campanha do CFN da FEB: “O Evangelho no Lar e no Coração”. 
    <p>Estimular o estudo interpretativo do Evangelho de Jesus, à luz da Doutrina Espírita.
    <p> <strong> 7 – A CAPACITAÇÃO DO TRABALHADOR ESPÍRITA; </strong>
    <p> <strong> Objetivos </strong>
    <p>Assegurar permanente capacitação dos trabalhadores espíritas para a gestão do Centro Espírita.
    <p>Assegurar permanente capacitação dos trabalhadores espíritas para todas as atividades doutrinárias, assistenciais, administrativas e de unificação. 
    <p>Assegurar permanente capacitação dos trabalhadores espíritas para acolher, consolar, esclarecer, orientar e integrar as pessoas que chegam ao Centro Espírita, atendendo-as em suas necessidades espirituais, morais e materiais. 
    <p>Estimular o relacionamento intra e interpessoal dos trabalhadores do Centro Espírita, buscando seu bem-estar e a convivência fraterna indispensável à execução das tarefas. 

    <p> <strong> 8 – A PARTICIPAÇÃO NA SOCIEDADE. </strong>
    <p> <strong> Objetivos </strong>
    <p>    Participar de forma mais efetiva junto à sociedade organizada e aos órgãos do Poder Público, contribuindo no encaminhamento de assuntos de interesse social, sempre de forma compatível com os princípios espíritas. 
    <p>   Estimular o atendimento solidário a pessoas e comunidades em vulnerabilidade e risco social, respeitando-se a legislação vigente. 
    <p>   Desenvolver programas de atividades institucionais, doutrinárias e promocionais, utilizando a arte, segundo os princípios e valores éticos e morais do Espiritismo

